def check(s):
    temp=104
    for i in range(len(s)):
        temp += int(b.index(s[i])) * (i+1)
    return temp%103

def b_array(s):
    temp=start
    for i in range(len(s)):
        temp+=encoding[int(b.index(s[i]))]
    temp += encoding[check(s)] + stop + '11'
    return temp

def bar(a):
    b_size=175
    t.screen.setup(width=len(a)+300, height=b_size+120, startx=None, starty=None)
    t.pu()
    t.setx(-len(a)//2)
    t.sety(-b_size//2)
    t.pd()
    t.speed(0)
    t.right(90)
    for i in range (len(a)):
        if i%2==0:
            t.left(90)
            t.pu()
            t.fd(1)
            t.pd()
            t.left(90)
        else:
            t.right(90)
            t.pu()
            t.fd(1)
            t.pd()
            t.right(90)

        if a[i]=='0':
            t.pu()
            t.fd(b_size)
        else:
            t.pd()
            t.fd(b_size)
    

